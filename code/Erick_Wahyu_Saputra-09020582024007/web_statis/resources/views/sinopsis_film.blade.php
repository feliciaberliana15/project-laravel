                Shang-Chi and the Legend of the Ten Rings (2021) : <br />
                <br />
                Kisah Shang-Chi bermula dari legenda sepuluh gelang ajaib yang dimiliki oleh Xu Wenwu sejak ribuan tahun lalu. <br />
                Gelang tersebut memiliki magis luar biasa dan memberikan kekuasaan juga kekuatan tak terhingga bagi pemegangnya. <br />
                Berkat itu pula, Xu Wenwu mendirikan organisasi Ten Rings dan menaklukkan banyak kerajaan, menyusup pemerintahan, dan menjalankan roda bisnis di berbagai wilayah di dunia selama bertahun-tahun. <br />
                Hingga pada 1996, Xu Wenwu menemukan soal legenda desa ajaib Ta Lo. Menurut legenda, desa tersebut memiliki kekuatan yang lebih hebat dan Xu Wenwu berambisi untuk memilikinya. <br />
                Akan tetapi, kala dirinya berusaha menemukan Ta Lo, ia justru bertemu seorang perempuan dan jatuh cinta kepadanya, Ying Li, seorang penjaga desa itu dan berhasil mengalahkan Xu Wenwu. <br />
                Singkat cerita, keduanya yang semula bertarung malah jatuh cinta pun memutuskan untuk bersama. Pernikahan mereka melahirkan dua anak, Xu Shang-Chi dan Xu Xialing. Xu Wenwu pun merasa tujuan hidupnya terpenuhi. <br />
                <br />
                Namun takdir berkata lain. Ying Li meninggal dunia dan mengubah karakter Xu Wenwu. Ia kemudian mendidik Xu Shang-Chi menjadi pembunuh kelas kakap sejak usia tujuh tahun. Xialing diam-diam mengikuti latihan yang diberikan kepada kakaknya itu. <br />
                Pada saat Shang-Chi usia 14 tahun, ia diberi tugas untuk membunuh pemimpin Iron Gang. Namun setelah melaksanakan misi tersebut, Shang-Chi kabur dan memutuskan menjalani kehidupan baru dengan nama Shaun di San Fransisco. <br />
                Di kota San Fransisco, Shaun tumbuh menjadi pemuda yang mampu 4 bahasa namun dirinya kini hanya menjadi petugas valet hotel bintang lima. Ia bekerja bersama sahabatnya sejak tiba di San Fransisco, Katy (Awkwafina). <br />
                Keduanya amat menikmati kehidupan santai dan tanpa beban, meski sahabat dan keluarga Katy kerap mendorong mereka untuk mencari pekerjaan yang lebih baik. Namun mereka lebih gemar sebagai petugas valet dan karaoke. <br />
                Suatu kali, Shang-Chi dan Katy mendapatkan serangan dari segerombol orang tak dikenal kala mereka naik bus umum. Mereka ingin merebut kalung liontin Shang-Chi yang merupakan warisan Ying Li. <br>
                Di tengah kondisi genting tersebut, Shang-Chi terpaksa mengeluarkan kemampuan beladiri yang selama ini ia simpan rapat-rapat. <br />
                Kemampuan Shang-Chi yang tak diketahui oleh Katy tersebut membawa keduanya ke fakta demi fakta yang terungkap terkait latar belakang dan konflik keluarga Xu, kemampuan sepuluh gelang, hingga nasib umat manusia. <br />
