			<p>Manfaat sayur sayuran secara garis besar adalah sumber serat, vitamin, dan mineral yang cukup baik bagi tubuh dan kesehatan manusia. Ada banyak jenis sayuran yang ada di Indonesia yang berkhasiat untuk meningkatkan kesehatan dan metabolisme tubuh.<br><br>

            Secara umum manfaat sayuran adalah sebagai berikut :<br>

            <p>1. Menyehatkan pencernaan</p>
			Buah dan sayur banyak mengandung serat alami yang mudah dicerna. Karena mudah unutk menyerap air, serat ini dapat meringankan atau mencegah sembelit, karena mendorong keinginan buang air besar menjadi lebih rutin dan lancar.
            Diet buah-buahan yang menambah asupan serat dan air juga dapat memberi efek kenyang yang lebih lama, sehingga dapat membantu menunda lapar dan mampu menjaga berat badan.
            <p>2. Menurunkan tekanan darah</p>
			Diet yang mencakup banyak buah-buahan dan sayuran dikaitkan dengan tekanan darah yang lebih rendah.
			Sebuah penelitian mengungkapkan, orang yang  tekanan darah tinggi yang menjalani diet buah, sayuran, produk susu rendah lemak, serta membatasi dan mengurangi asupan lemak, dapat mengalami penurunan tekanan darah menyerupai efek obat.
            <p>3. Menurunan risiko penyakit jantung</p>
			Makin banyak jumlah rata-rata konsumsi buah dan sayuran per hari , maka peluang terjadinya penyakit kardiovaskular menjadi makin rendah.
			Pada sebuah penelitian didapatkan bahwa orang yang menjalani diet buah-buahan atau mengonsumsi rata-rata delapan atau lebih takaran sajibuah dan sayur perhari, berpeluang 30 persen lebih rendah untuk mengalami serangan jantung dan stroke.
			Hasil penelitian tersebut menjelaskan bahwa buah yang paling penting kontribusinya untuk menurunkan risiko penyakit jantung adalah jeruk, lemon, limau, dan anggur. Sedangkan jenis sayurannya adalah sayuran hijau, seperti bayam, brokoli, kubis, dan selada.
